    public void updateEmp() {
        String sql = "select * from employee13 where id = ?";
        Scanner scan = new Scanner(System.in);
        
        
        try {
            Class.forName(dpath);
            con = DriverManager.getConnection(url,user,password);
            ps = con.prepareStatement(sql);
            int id = Integer.parseInt(idText.getText());
            ps.setInt(1, id);
            
            rs = ps.executeQuery();
            
            if(rs.next()) {
               nameText.setText(rs.getString(2));
               nameText.setEditable(false);
               emailText.setText(rs.getString(3));
               emailText.setEditable(false);
               salarytext.setText(String.valueOf(rs.getInt(4)));
               salarytext.setEditable(false);
               phoneNo.setText(String.valueOf(rs.getLong(5)));
               phoneNo.setEditable(false);
               addresstext.setText(rs.getString(6));
               addresstext.setEditable(false);
            }
            else {
                JOptionPane.showMessageDialog(rootPane, "Wrong Id Entered, Please Enter Valid Id between 1 to 10","Warning",JOptionPane.WARNING_MESSAGE);
               
            }
            
        }
        catch(Exception e) {
        e.printStackTrace();
        }
    }


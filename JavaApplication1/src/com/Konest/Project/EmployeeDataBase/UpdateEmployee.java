    public void updatedata(){
        String sql = "UPDATE employee13 SET name = ?, email = ?, salary = ?, phoneNo = ?, address = ? WHERE id = ?";
        Scanner scan = new Scanner(System.in);
        try {
        Class.forName(dpath);
        con = DriverManager.getConnection(url,user,password);
        ps = con.prepareStatement(sql);
        String name = nameText.getText();
        String email = emailText.getText();
        int salary = Integer.parseInt(salarytext.getText());
        Long phoneno = Long.parseLong(phoneNo.getText());
        String address = addresstext.getText();
        int id = Integer.parseInt(idText.getText());
        ps.setString(1,name);
        ps.setString(2, email);
        ps.setInt(3, salary);
        ps.setLong(4, phoneno);
        ps.setString(5, address);
        ps.setInt(6, id);
        int nora = ps.executeUpdate();
        if(nora > 0) {
            JOptionPane.showMessageDialog(rootPane, "Employee got Updated Successfully");
        }
        else {
            JOptionPane.showMessageDialog(rootPane, "Invalid Credentials", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        }
        catch(Exception e) {
            e.printStackTrace();
        }
        
        
    }
